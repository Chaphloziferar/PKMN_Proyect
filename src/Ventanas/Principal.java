    public void reproducir(){
        try{
            sonido.play();
        }catch(BasicPlayerException e){
            System.out.println(e);
        }
    }
    
    public void abrirFichero(String archivo){
        try{
            sonido.open(new File(archivo));
        }catch(BasicPlayerException e){
            System.out.println(e);
        }
    }
    
    public void mostrarVentana(){
        Seleccion_PKMN sp = new Seleccion_PKMN();
        sp.setVisible(true);
        try {
            this.sonido.stop();
        } catch (BasicPlayerException e) {
            System.out.println(e);
        }
        this.dispose();
    }
    

