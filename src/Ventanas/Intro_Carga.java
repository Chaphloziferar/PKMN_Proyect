    public Intro_Carga() {
        initComponents();
        this.setLocationRelativeTo(null);
        
        sonido = new BasicPlayer();
        abrirFichero("Music_PKMN"+File.separator+"Pok√©mon Rojo FuegoVerde Hoja OST - 01 - Opening.mp3");
        reproducir();
        
        al = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if(ProgressBar.getValue() < 120){
                    ProgressBar.setValue(ProgressBar.getValue()+3);
                }else{
                    t.stop();
                    mostrarVentana();
                }
            }
        };
        
        t = new Timer(325, al);
        t.start();
    }
    
    
    public void reproducir(){
        try{
            sonido.play();
        }catch(BasicPlayerException e){
            System.out.println(e);
        }
    }
    
    public void abrirFichero(String archivo){
        try{
            sonido.open(new File(archivo));
        }catch(BasicPlayerException e){
            System.out.println(e);
        }
    }
    
    public void mostrarVentana(){
        Principal p = new Principal();
        p.setVisible(true);
        this.dispose();
    }
    

